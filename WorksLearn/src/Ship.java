
import javax.swing.*;
import java.awt.*;

// Класс для отрисовки парусника, наследуется от JPanel
public class Ship extends JPanel {

    // Переопределение метода paint для отрисовки графики
    @Override
    public void paint(Graphics g) {
        // Вызов родительского метода для очистки панели
        super.paint(g);
        // Приведение Graphics к Graphics2D для дополнительных возможностей
        Graphics2D g2d = (Graphics2D) g;
        // Получение ширины и высоты панели
        int w = getWidth();
        int h = getHeight();

        // Определение цветов для элементов рисунка
        Color sailColor = Color.WHITE; // Цвет паруса (белый)
        Color skyColor = new Color(135, 205, 229); // Цвет неба (голубой)
        Color sunColor = new Color(255, 206, 52); // Цвет солнца (желто-оранжевый)
        Color boatColor = new Color(134, 69, 52); // Цвет корпуса лодки (коричневый)
        Color oceanColor = new Color(1, 123, 143); // Цвет океана (сине-зеленый)

        // Отрисовка неба (прямоугольник, занимающий верхние 80% высоты)
        g2d.setColor(skyColor);
        g2d.fillRect(
                0, // Начальная x-координата
                0, // Начальная y-координата
                w, // Ширина (вся ширина панели)
                (int) (h * 0.8) // Высота (80% высоты панели)
        );

        // Отрисовка океана (прямоугольник в нижней части, 20% высоты)
        g2d.setColor(oceanColor);
        g2d.fillRect(
                0, // Начальная x-координата
                (int) (h * 0.8), // Начальная y-координата (начало ниже неба)
                w, // Ширина (вся ширина панели)
                (int) (h * 0.2) // Высота (20% высоты панели)
        );

        // Отрисовка солнца (овал в правом верхнем углу)
        g2d.setColor(sunColor);
        g2d.fillOval(
                (int) (w * 0.8), // x-координата (80% ширины)
                (int) (h * -0.2), // y-координата (часть солнца выше панели)
                (int) (w * 0.4), // Ширина овала (40% ширины)
                (int) (h * 0.4) // Высота овала (40% высоты)
        );

        // Отрисовка паруса (треугольник из 3 точек)
        g2d.setColor(sailColor);
        int[] sx = {(int) (w * 0.51), (int) (w * 0.66), (int) (w * 0.51)}; // x-координаты вершин
        int[] sy = {(int) (h * 0.3), (int) (h * 0.45), (int) (h * 0.6)}; // y-координаты вершин
        g2d.fillPolygon(sx, sy, 3); // Заполнение треугольника

        // Отрисовка корпуса лодки (многоугольник из 8 точек)
        g2d.setColor(boatColor);
        int[] bx = {
                (int) (w * 0.25), (int) (w * 0.49), (int) (w * 0.49),
                (int) (w * 0.51), (int) (w * 0.51), (int) (w * 0.75),
                (int) (w * 0.70), (int) (w * 0.30)
        }; // x-координаты вершин корпуса
        int[] by = {
                (int) (h * 0.65), (int) (h * 0.65), (int) (h * 0.3),
                (int) (h * 0.3), (int) (h * 0.65), (int) (h * 0.65),
                (int) (h * 0.85), (int) (h * 0.85)
        }; // y-координаты вершин корпуса
        g2d.fillPolygon(bx, by, 8); // Заполнение многоугольника
    } // paint()

    // Главный метод для запуска приложения
    public static void main(String[] args) {
        // Создание окна с заголовком "Рисунок парусника"
        JFrame frame = new JFrame("Рисунок парусника");
        // Закрытие программы при закрытии окна
        frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        // Создание экземпляра класса DrawingBoat для отрисовки
        Ship myPic = new Ship();
        // Добавление панели с рисунком в окно
        frame.add(myPic);
        // Установка размера окна (800x600 пикселей)
        frame.setSize(800, 600);
        // Сделать окно видимым
        frame.setVisible(true);
    } // main()
}